# Deskripsi
# Jika A dan B adalah bilangan prima, apakah AB (A ditempelkan dengan B) juga prima? Bisa ya bisa tidak. Jika A = 2 dan B = 3, maka 23 juga prima. Namun jika A = 2 dan B = 5, maka 25 bukan prima.

# Diberikan dua buah bilangan bulat, M dan N, Anda diminta untuk mencari pasangan-pasangan bilangan-bilangan prima A dan B (M ≤ A, B ≤ N) sehingga AB (A ditempelkan dengan B) juga prima.

# Format Masukan
# Baris pertama berisi dua buah bilangan bulat M dan N.

# Format Keluaran
# Keluarkan pasangan-pasangan bilangan prima yang memenuhi syarat di atas, satu pasang setiap baris dengan kedua bilangan prima dipisahkan sebuah spasi. Jika tidak ada pasangan yang memenuhi, keluarkan TIDAK ADA. Urutkan pasangan-pasangan tersebut, sehingga bilangan pertama terurut menaik, dan untuk bilangan pertama yang sama, bilangan kedua terurut menaik.

# Contoh Masukan 1
# 2 10
# Contoh Keluaran 1
# 2 3
# 3 7
# 5 3
